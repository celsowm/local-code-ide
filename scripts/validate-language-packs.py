#!/usr/bin/env python3
"""Validate bundled language-pack manifest and launcher payloads."""

from __future__ import annotations

import argparse
import hashlib
import json
import os
import platform
import re
import subprocess
import sys
from pathlib import Path
from typing import Iterable


PINNED_VERSION_RE = re.compile(r"^[0-9A-Za-z][0-9A-Za-z._-]*$")

